package mainClass;

public class roster 
{
	private String playerFirstName;
	private String playerLastName;
	private int playerJerseyNumber;
	private String playerPosition;
	private String playerCollege;
	private int playerStarting;
		
	public void setFirstName(String s)
	{
		playerFirstName = s;
	}
	public String getFirstName()
	{
		return playerFirstName;
	}
	public void setLastName(String s)
	{
		playerLastName = s;
	}
	public String getLastName()
	{
		return playerLastName;
	}
	public void setJerseyNumber(int i)
	{
		playerJerseyNumber= i;
	}
	public int getJerseyNumber()
	{
		return playerJerseyNumber;
	}
	public void setPosition(String s)
	{
		playerPosition = s;
	}
	public String getPosition()
	{
		return playerPosition;
	}
	public void setCollege(String s)
	{
		playerCollege = s;
	}
	public String getCollege()
	{
		return playerCollege;
	}
	public void setStarting(int i)
	{
		playerStarting = i;
	}
	public int getStarting()
	{
		return playerStarting;
	}
} 